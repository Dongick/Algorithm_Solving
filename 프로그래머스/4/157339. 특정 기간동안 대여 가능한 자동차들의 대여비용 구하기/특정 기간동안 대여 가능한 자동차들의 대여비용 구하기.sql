SELECT C.CAR_ID, C.CAR_TYPE, ROUND(C.DAILY_FEE * (100 - D.DISCOUNT_RATE) * 0.01 * 30) AS FEE
FROM CAR_RENTAL_COMPANY_CAR C JOIN CAR_RENTAL_COMPANY_DISCOUNT_PLAN D ON C.CAR_TYPE = D.CAR_TYPE
WHERE C.CAR_ID NOT IN (SELECT CAR_ID 
       FROM CAR_RENTAL_COMPANY_RENTAL_HISTORY
       WHERE START_DATE <= '2022-11-30' AND END_DATE >= '2022-11-01'
       GROUP BY CAR_ID) AND DURATION_TYPE = '30일 이상'
HAVING FEE >= 500000 AND FEE < 2000000
ORDER BY FEE DESC, C.CAR_TYPE ASC, C.CAR_ID DESC